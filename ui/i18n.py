def set_language(language: str) -> str:
    global _LANGUAGE, _LANGUAGE_EXPLICIT
    _LANGUAGE = normalize_language(language, default="en")
    _LANGUAGE_EXPLICIT = True
    return _LANGUAGE


def current_language() -> str:
    return normalize_language(_LANGUAGE, default="en")


def get_language() -> str:
    return current_language()


def has_explicit_language() -> bool:
    return bool(_LANGUAGE_EXPLICIT)


def tr(key: str) -> str:
    text = str(key or "")
    if _LANGUAGE == "en":
        return text
    return _TRANSLATIONS.get(_LANGUAGE, {}).get(text, text)


def language_display_name(language: str) -> str:
    code = normalize_language(language, default="en")
    return "Türkçe" if code == "tr" else "English"
